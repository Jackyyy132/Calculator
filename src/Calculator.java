    private void EqualsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EqualsBtnActionPerformed
        ClickSound();
        //https://stackoverflow.com/questions/2605032/is-there-an-eval-function-in-java        
        ScriptEngineManager manager = new ScriptEngineManager();
        ScriptEngine engine = manager.getEngineByName("js");
        String Equation = Display.getText();
        Object result;
        String Result = "";
        boolean Correct = false;
        
        
        //we start at int i = 1 because we cant look at position 0-1 and even if the ( is in the first position, it wont need a * sign on it.
        for (int i = 1; i < Display.getText().length(); i++){
            //variable to get the individual character
            char Term = Equation.charAt(i);
            //variable to get the individual character behind the first one
            char Term1 = Equation.charAt(i-1);   
            if (Character.toString(Term).matches("\\(") && ((!Character.toString(Term1).matches("\\+")) && (!Character.toString(Term1).matches("-")) && (!Character.toString(Term1).matches("/")) && (!Character.toString(Term1).matches("\\*")))){
                Equation = Equation.substring(0, i) + "*" + Equation.substring(i, Equation.length());
                break;
            }
        } 
        
        try {
            result = engine.eval(Equation);
            Correct = true;
            Result = result.toString();
        } 
        catch (ScriptException ex) { 
            Font f = new Font("Tahoma",Font.PLAIN,20);
            Display.setFont(f);
            Display.setText("Error. Press 'Clear All' to Restart Calculator.");
            OpenBracketBtn.setEnabled(false);
            CloseBracketBtn.setEnabled(false);
            DivideBtn.setEnabled(false);
            MultiplyBtn.setEnabled(false);
            AddBtn.setEnabled(false);
            MinusBtn.setEnabled(false);
            OneBtn.setEnabled(false);
            TwoBtn.setEnabled(false);
            ThreeBtn.setEnabled(false);
            FourBtn.setEnabled(false);
            FiveBtn.setEnabled(false);
            SixBtn.setEnabled(false);
            SevenBtn.setEnabled(false);
            EightBtn.setEnabled(false);
            NineBtn.setEnabled(false);
            ZeroBtn.setEnabled(false);
            DotBtn.setEnabled(false);
            EqualsBtn.setEnabled(false);
            DeleteBtn.setEnabled(false);      
        }
        
        
        try {
            History(Equation, Result);
        } catch (IOException ex) {
            Logger.getLogger(Calculator.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        if (Correct == true){
            Display.setText(Result);
        }
        
        
               
    }//GEN-LAST:event_EqualsBtnActionPerformed

