    public void History(String Equation, String Result) throws IOException{
        File file = new File("History.txt");
        FileWriter fw = new FileWriter(file, true);
        PrintWriter pw = new PrintWriter(fw);
        if (Result.equals("")){
            
            Result = "Error";
        }
      
        pw.println(Equation + " = " + Result);
        pw.close();
        
        
        
    }

